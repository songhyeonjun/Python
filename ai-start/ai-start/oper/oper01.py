# 연산자: 간단한 처리 위한 기호
# 연산은 cpu담당.
n1 = 222; n2 = 100;
# 산술연산자: 사칙(+ - * / // %)
# ctrl + alt +화살표 아래: 한줄 복사
# ctrl + d: 한줄 삭제
print(n1 , '과 ', n2, '의 합은: ', n1 + n2)
print(n1 , '과 ', n2, '의 차는: ', n1 - n2)
print(n1 , '과 ', n2, '의 곱은: ', n1 * n2)
print(n1 , '과 ', n2, '의 나눗셈은: ', n1 / n2)
print(n1 , '과 ', n2, '의 나눗셈은: ', n1 // n2)
print(n1 , '과 ', n2, '의 나눗셈의 나머지는 : ', n1 % n2)